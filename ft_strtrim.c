#include "libft.h"

static int	in_set(char c, const char *set)
{
	size_t	i;

	i = 0;
	while (set[i])
	{
		if (set[i] == c)
			return (1);
		i++;
	}
	return (0);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	size_t	len;
	char	*strtrimmed;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && in_set(s1[start], set))
		start++;
	while (end > start && in_set(s1[end - 1], set))
		end--;
	len = end - start;
	strtrimmed = malloc(len + 1);
	if (!strtrimmed)
		return (NULL);
	if (len)
		ft_memcpy(strtrimmed, s1 + start, len);
	strtrimmed[len] = '\0';
	return (strtrimmed);
}
